public class SubTopicDetail {


    int chapterNumber;
    String chapterTopicTitle;
    int PageNumbers;
    int timeInMin;


    public int getChapterNumber() {
        return chapterNumber;
    }

    public void setChapterNumber(int chapterNumber) {
        chapterNumber = chapterNumber;
    }

    public String getChapterTopicTitle() {
        return chapterTopicTitle;
    }

    public void setChapterTopicTitle(String chepterTopicTitle) {
        this.chapterTopicTitle = chepterTopicTitle;
    }

    public int getPageNumbers() {
        return PageNumbers;
    }

    public void setPageNumbers(int pageNumbers) {
        PageNumbers = pageNumbers;
    }

    public int getTimeInMin() {
        return timeInMin;
    }

    public void setTimeInMin(int timeInMin) {
        this.timeInMin = timeInMin;
    }


}
